def localize_audit_value(raw: object, user=None, request=None) -> str:
    if not raw:
        return ""
    language = language_for(user, request)
    try:
        data = json.loads(str(raw))
    except (TypeError, ValueError, json.JSONDecodeError):
        return str(raw)

    def convert(value):
        if isinstance(value, dict):
            return {
                AUDIT_FIELD_LABELS.get(str(key), (str(key), str(key)))[1 if language == "en" else 0]: convert(item)
                for key, item in value.items()
            }
        if isinstance(value, list):
            return [convert(item) for item in value]
        if isinstance(value, str):
            return translate_value(value, language)
        return value

    return json.dumps(convert(data), ensure_ascii=False, indent=2)


