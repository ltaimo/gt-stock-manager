def translate_message(text: object, language: str = "pt") -> str:
    value = translate_text(text, "pt")
    if normalize_language(language) == "pt":
        return value
    if value in MESSAGE_EN:
        return MESSAGE_EN[value]
    if value in PHRASES_EN:
        return PHRASES_EN[value]

    patterns = (
        (r"^(.+) é obrigatóri[oa]\.$", "{field} is required."),
        (r"^(.+) contém um valor inválido\.$", "{field} contains an invalid value."),
        (r"^Verifique o campo (.+)\.$", "Check the {field} field."),
        (r"^(.+) inválid[oa]\.$", "Invalid {field}."),
        (r"^(.+) não pode ficar vazio\.$", "{field} cannot be empty."),
        (r"^(.+) deve ser um número válido\.$", "{field} must be a valid number."),
        (r"^(.+) deve ser um número finito\.$", "{field} must be a finite number."),
        (r"^(.+) não pode exceder (\d+) caracteres\.$", "{field} cannot exceed {limit} characters."),
        (r"^Adicione pelo menos um[a]? (.+)\.$", "Add at least one {field}."),
        (r"^O item (.+) não tem stock disponível\.$", "Item {item} has no available stock."),
        (r"^A quantidade para (.+) deve ser superior a zero\.$", "The quantity for {item} must be greater than zero."),
        (r"^Stock insuficiente para (.+)\.$", "Insufficient stock for {item}."),
        (r"^A quantidade aprovada para (.+) é inválida\.$", "The approved quantity for {item} is invalid."),
        (r"^A quantidade aprovada para (.+) excede a quantidade requisitada\.$", "The approved quantity for {item} exceeds the requested quantity."),
        (r"^Esta requisição deve ser aprovada por (.+)\.$", "This requisition must be approved by {item}."),
        (r"^Este processo deve ser aprovado pelo perfil (.+)\.$", "This process must be approved by the {item} profile."),
        (r"^Stock resetado com sucesso\. Produtos afetados: (.+); quantidade removida: (.+)\.$", "Stock reset successfully. Products affected: {field}; quantity removed: {item}."),
    )
    for pattern, replacement in patterns:
        match = re.match(pattern, value, flags=re.IGNORECASE)
        if not match:
            continue
        groups = match.groups()
        field = translate_text(groups[0], "en") if groups else ""
        return replacement.format(
            field=field.lower() if replacement.startswith("Invalid ") else field,
            limit=groups[1] if len(groups) > 1 else "",
            item=groups[-1] if groups else "",
        )
    return value


