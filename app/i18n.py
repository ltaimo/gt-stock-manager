def normalize_language(language: str | None) -> str:
    return language if language in SUPPORTED_LANGUAGES else "pt"


def language_for(user=None, request=None) -> str:
    if user is not None:
        return normalize_language(getattr(user, "preferred_language", None))
    if request is not None:
        return normalize_language(request.session.get("language"))
    return "pt"


def localized_name(entity, user=None, request=None) -> str:
    if entity is None:
        return ""
    if language_for(user, request) == "en":
        english = str(getattr(entity, "name_en", "") or "").strip()
        if english:
            return english
    return str(getattr(entity, "name", "") or "")


def translate_key(key: str, language: str = "pt") -> str:
    lang = normalize_language(language)
    return KEY_TRANSLATIONS[lang].get(key, KEY_TRANSLATIONS["pt"].get(key, key))


def translate_text(text: object, language: str = "pt") -> str:
    value = "" if text is None else str(text)
    if normalize_language(language) == "pt":
        return PHRASES_PT.get(value, value)
    return PHRASES_EN.get(value, value)


def translate_value(value: object, language: str = "pt") -> str:
    text = "" if value is None else str(value)
    translated = SYSTEM_VALUES.get(text)
    if not translated:
        return translate_text(text, language)
    return translated[1] if normalize_language(language) == "en" else translated[0]


